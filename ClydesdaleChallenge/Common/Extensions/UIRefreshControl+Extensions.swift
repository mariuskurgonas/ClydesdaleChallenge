import Foundation
import UIKit

extension UIRefreshControl {
    func beginRefreshingManually() {
        if let scrollView = superview as? UIScrollView {
            scrollView.setContentOffset(CGPoint(x: 0, y: -frame.height), animated: true)
        }
        beginRefreshing()
    }
}
